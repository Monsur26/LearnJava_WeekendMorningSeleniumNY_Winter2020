package operators;

public class EqualityAndRelationalOperator {
	
	// ==
	// !=
	// >
	// <
	// <=
	// >=
	
	
	public static void main(String[] args) {
		
		
		int num1=34;
		int num2=44;
		
		if(num1==num2) {
				System.out.println("num is 34");
	}
		if(num1!=num2) {
			System.out.println("num is 34");
}
		if(num1>num2) {
			System.out.println("num is 34");
}
		if(num1<num2) {
			System.out.println("num is 34");
}

		if(num1>=num2) {
			System.out.println("num is 34");
}
		if(num1<=num2) {
			System.out.println("num is 34");
}

		
	
	
	

}
}

package basic;

public class UseVariable {
	
	
	
	
	
	//Variable is a container which can hold values
	
	           //int is a Data Type, x is variable name and 5 is value.
			  //DataType Variable name= value; To close any statement use semicolon.
	
	//Global Variable
	
	int x=5; //Declare and initialize value 
	int y;  //Declare variable
	
	
	//Declare
	static int a;
	int b;
	int c;
	
	//Declare an initialize
	int p=46;
	int t=8;
	
	//Variable name convention:
	//Variable Name must be meaningful.
	int number=90;
	static int age=24;
	String firstName="James";
	static int JamesAge=60;
	
	//Constant Variable always capital letter. 
	String MAX_WIDTH="34";
	final String MAX_WIDTH_FOR_FABRIC="36"; //final means fixed value.
	
	public static void main(String[] args) {//method body start point
	//Driver method/main method 
	
	
	//method body
		
		
	//Local Variable
		int g=42;
		a=7;
	
	System.out.println(g);
	System.out.println(a);
	System.out.println(age);
	System.out.println("My age is " +age);
	System.out.println("Jimmys' age is " +JamesAge);
	
	
	}//method body end point
	
	
	
	
	
	
	
	
	
	
	
	

}
